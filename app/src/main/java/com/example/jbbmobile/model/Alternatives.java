package com.example.jbbmobile.model;

public class Alternatives {
    private int idAlternative;
    private int alternativeLetter;
    private int alternativeDescription;
    private int idQUestion;
    

    public int getIdAlternative() {
        return idAlternative;
    }

    public void setIdAlternative(int idAlternative) {
        this.idAlternative = idAlternative;
    }

    public int getAlternativeLetter() {
        return alternativeLetter;
    }

    public void setAlternativeLetter(int alternativeLetter) {
        this.alternativeLetter = alternativeLetter;
    }

    public int getAlternativeDescription() {
        return alternativeDescription;
    }

    public void setAlternativeDescription(int alternativeDescription) {
        this.alternativeDescription = alternativeDescription;
    }

    public int getIdQUestion() {
        return idQUestion;
    }

    public void setIdQUestion(int idQUestion) {
        this.idQUestion = idQUestion;
    }
}
